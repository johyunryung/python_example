#더하기 함수
def plus(a,b):
    return a+b
print(plus(10,20))

#빼기 함수
def minus(a,b):
    return a-b
print(minus(10,20))

#곱셈 함수
def multiply(a,b):
    return a*b
print(multiply(10,20))

#나누기 함수
def divide(a,b):
    return a/b
print(divide(10,20))

#몫 구하는 함수
def rest(a,b):
    return a%b
print(rest(55,20))
